package org.opentutorials.javatutorials.variables;

public class IntegerDemo {
    public static void main(String[] args) {
        int a; // int(integer의 약자)는 데이터형식, a는 변수의 이름 - 즉 a라는 변수에는 숫자만 들어갈 수 있다.
        a = 1;
        System.out.println(a+1);
        a = 2; // 변수에 값 재할당
        System.out.println(a+1);
    }
}

//정수(Int) - 소수점이 없는 수
//실수(Double) - 소수점이 있는 수