package org.opentutorials.javatutorials.variables;

public class StringDemo {
    public static void main(String[] args) {
        String first; //String 타입에는 문자열만 담을 수 있다.
        first = "coding";

        //String first = "coding" 으로 위 두줄을 표현할 수 도 있다.
        String a, b; //변수도 두개이상 선언가능
        a = "coding";
        b = "everybody";
        System.out.println(a+b); // 변수 두고래르 합쳐서 출력가능
    }
}
