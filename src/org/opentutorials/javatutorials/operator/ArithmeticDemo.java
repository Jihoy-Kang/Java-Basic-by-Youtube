package org.opentutorials.javatutorials.operator;

public class ArithmeticDemo {
    public static void main(String[] args){
        int result = 1 + 2;
        System.out.println(result);

        result = result - 1;
        System.out.println(result);

        result = result * 2;
        System.out.println(result);

        result = result / 2;
        System.out.println(result);

        result = result + 8;
        System.out.println(result);

        result = result % 7; //10을 7로 나눴을때 나머지 결과값
        System.out.println(result);
    }
}
